function Get-Criticality {
    param([string]$resourceName, [string]$resourceGroup, [string]$subscriptionName, [string]$resourceType)
    $name = "$resourceName $resourceGroup $subscriptionName".ToLower()

    # Dev/Test subscriptions — do not assess criticality; customer to determine DR need
    # Check subscription name first (most reliable signal)
    if ($subscriptionName -match '\b(dev|test|tst|poc|sandbox|uat|qa|stage|stg)\b') {
        return "Not Assessed (Dev/Test)"
    }

    # Production indicators — labels are SUGGESTED based on naming analysis only
    if ($name -match '\b(prod|prd|production|live)\b') {
        # Infrastructure/data services → suggest Critical
        if ($resourceType -match 'virtualmachines$|managedclusters$|sql|mysql|postgres|documentdb|storage|keyvault|netapp|recoveryservices|apimanagement|eventhub|servicebus|avs|redisenterprise|datafactory') {
            return "Critical (Suggested)"
        }
        return "High (Suggested)"
    }
    # Non-production environment indicators in resource/RG name
    if ($name -match '\b(tst|test|dev|poc|stage|stg|sandbox|uat|qa)\b') {
        return "Not Assessed (Dev/Test)"
    }
    # Default — undetermined; suggest based on resource type
    if ($resourceType -match 'virtualmachines$|managedclusters$|sql|mysql|postgres|documentdb|keyvault|netapp|apimanagement|avs') {
        return "Medium (Suggested)"
    }
    return "Low (Suggested)"
}

function Get-Priority {
    param([string]$criticality, [string]$zoneStatus, [string]$crossRegionStatus, [string]$location)
    # Dev/Test — not assessed; customer to determine if DR is relevant
    if ($criticality -like "*Dev/Test*" -or $criticality -like "*Not Assessed*") {
        return "Not Prioritised (Dev/Test — Customer to Assess DR Need)"
    }
    # All priorities are SUGGESTED — actual priority must be confirmed by customer
    if ($criticality -like "Critical*")  { return "P1 (Suggested — Confirm with Customer)" }
    if ($criticality -like "High*")      { return "P2 (Suggested — Confirm with Customer)" }
    if ($criticality -like "Medium*") {
        if ($zoneStatus -in @("NonZonal", "LocallyRedundant")) { return "P2 (Suggested — Confirm with Customer)" }
        return "P3 (Suggested — Confirm with Customer)"
    }
    return "P4 (Suggested — Confirm with Customer)"
}

function Get-DRTargetRegion {
    param([string]$location, [string]$pairedRegion)
    switch ($location.ToLower()) {
        "qatarcentral"  { return "West Europe (westeurope) — No paired region for Qatar Central; customer's existing Landing Zone in West Europe. Note: Azure Backup RoC (preview) uses Sweden Central or Switzerland North as vault region." }
        "westeurope"    { return "North Europe (northeurope) — Official Azure paired region for West Europe" }
        "northeurope"   { return "West Europe (westeurope) — Official Azure paired region for North Europe" }
        "uaenorth"      { return "UAE Central (uaecentral) — Official Azure paired region for UAE North" }
        default {
            if ($pairedRegion) { return "$pairedRegion — Official Azure paired region" }
            return "Customer to select — Review official Azure regional pairs for this region"
        }
    }
}

function Get-ZRContextNote {
    param([string]$location, [string]$zoneStatus)
    if ($location.ToLower() -eq "qatarcentral") {
        return "QATAR CENTRAL RESTRICTIONS (Microsoft Qatar Team): (1) Zone Redundancy is DISABLED — one AZ at full capacity, ZR services BLOCKED. (2) GRS/GZRS storage is NOT AVAILABLE — Qatar Central has no paired region; use Object Replication instead. (3) Native AKS Backup NOT available in Qatar Central — use Velero. (4) Key Vault does NOT sync across non-paired regions — custom Function-based sync required. (5) No paired region — customer to select DR region of choice; West Europe (existing LZ) is a common pattern. (6) Azure Backup standard CRR is NOT available for Qatar Central — preview 'Region of Choice (RoC)' feature via Microsoft Engineering supports IaaS VM/SQL/SAP/AFS workloads with vault in Sweden Central or Switzerland North."
    }
    if ($zoneStatus -in @("ZoneRedundant", "Zonal")) {
        return "Zone redundancy configured. For full BCDR: also consider cross-region DR as a second layer of protection — customer to confirm DR region of choice."
    }
    return "No zone redundancy. Cross-region DR is a consideration — customer to determine DR region of choice and confirm scope."
}

function Get-CurrentGapSummary {
    param([string]$zoneStatus, [string]$crossRegionStatus, [string]$drReady, [string]$geoRedundant, [string]$location)
    $gaps = @()
    if ($location.ToLower() -eq "qatarcentral" -and $zoneStatus -notin @("ZoneRedundant", "RedundantByDefault")) {
        $gaps += "No zone redundancy (blocked in Qatar Central)"
    }
    if ($crossRegionStatus -in @("Single-Region", "")) {
        $gaps += "No cross-region DR configured"
    }
    if ($drReady -eq "False" -or $drReady -eq "") {
        $gaps += "DRReady=False — no DR configured"
    }
    if ($geoRedundant -eq "False" -or $geoRedundant -eq "") {
        $gaps += "Not geo-redundant"
    }
    if ($gaps.Count -eq 0) { return "Partially configured — review details" }
    return $gaps -join " | "
}

