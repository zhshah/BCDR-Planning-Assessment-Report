#region ── Smart Workload Tier Classification ──────────────────────────────────
# Automatically classify resources into Production, Non-Production, Dev/Test, or Sandbox
# based on subscription name, management group (if available), resource tags, and naming patterns

function Get-WorkloadTier {
    <#
    .SYNOPSIS
        Classifies a resource's workload tier for appropriate BCDR recommendations
    .DESCRIPTION
        Returns tier classification (Production, Non-Production, Dev/Test, Sandbox)
        with confidence level and detection source. Used to adjust DR strategy and priority.
    #>
    param(
        [string]$resourceName, 
        [string]$resourceGroup, 
        [string]$subscriptionName,
        [hashtable]$tags = @{}
    )
    
    # Keywords for each tier (case-insensitive regex word boundaries)
    $prodKeywords = @('\bprod\b', '\bprod-', '-prod-', '-prod$', 'production', '\bprd\b', '\bprd-', '-prd-', '-prd$', '\blive\b')
    $nonprodKeywords = @('nonprod', 'non-prod', 'nonprd', '\bnp-', '-np-', '-np$')
    $devTestKeywords = @('\bdev\b', '\bdev-', '-dev-', '-dev$', 'development', '\btest\b', '\btest-', '-test-', '-test$', 'testing', '\btst\b', '\buat\b', '\buat-', '-uat-', '-uat$', '\bqa\b', '\bqa-', '-qa-', '-qa$', '\bstage\b', '\bstaging\b', '\bstg\b', '\bstg-', '-stg-', '-stg$')
    $sandboxKeywords = @('sandbox', '\bsbx\b', '\bsbx-', '-sbx-', '-sbx$', '\bpoc\b', 'proof-of-concept', '\bpoc-', '-poc-', '-poc$', 'demo', 'trial', 'playground', 'learning', '\blab\b', '\blab-')
    
    # Priority 1: Check subscription name (highest confidence)
    $subNameLower = $subscriptionName.ToLower()
    
    # Explicit production subscription
    foreach ($keyword in $prodKeywords) {
        if ($subNameLower -match $keyword) {
            return @{
                Tier = "Production"
                Confidence = "High"
                Source = "Subscription: $subscriptionName"
            }
        }
    }
    
    # Sandbox environment (lowest priority for DR)
    foreach ($keyword in $sandboxKeywords) {
        if ($subNameLower -match $keyword) {
            return @{
                Tier = "Sandbox"
                Confidence = "High"
                Source = "Subscription: $subscriptionName"
            }
        }
    }
    
    # Dev/Test environment
    foreach ($keyword in $devTestKeywords) {
        if ($subNameLower -match $keyword) {
            return @{
                Tier = "Dev/Test"
                Confidence = "High"
                Source = "Subscription: $subscriptionName"
            }
        }
    }
    
    # Non-prod (generic, not specifically dev/test)
    foreach ($keyword in $nonprodKeywords) {
        if ($subNameLower -match $keyword) {
            return @{
                Tier = "Non-Production"
                Confidence = "High"
                Source = "Subscription: $subscriptionName"
            }
        }
    }
    
    # Priority 2: Check resource tags (medium confidence)
    if ($tags -and $tags.Count -gt 0) {
        $envTags = @('Environment', 'Env', 'Tier', 'Stage', 'Workload', 'AppTier', 'Application-Tier')
        
        foreach ($tagKey in $envTags) {
            if ($tags.ContainsKey($tagKey)) {
                $tagValue = $tags[$tagKey].ToLower()
                
                if ($tagValue -match '(prod|production|prd|live)') {
                    return @{
                        Tier = "Production"
                        Confidence = "Medium"
                        Source = "Tag: $tagKey=$($tags[$tagKey])"
                    }
                }
                if ($tagValue -match '(sandbox|sbx|poc|demo|lab)') {
                    return @{
                        Tier = "Sandbox"
                        Confidence = "Medium"
                        Source = "Tag: $tagKey=$($tags[$tagKey])"
                    }
                }
                if ($tagValue -match '(dev|test|uat|qa|stage|staging|tst)') {
                    return @{
                        Tier = "Dev/Test"
                        Confidence = "Medium"
                        Source = "Tag: $tagKey=$($tags[$tagKey])"
                    }
                }
                if ($tagValue -match '(nonprod|non-prod|nonprd)') {
                    return @{
                        Tier = "Non-Production"
                        Confidence = "Medium"
                        Source = "Tag: $tagKey=$($tags[$tagKey])"
                    }
                }
            }
        }
    }
    
    # Priority 3: Check resource group name (low confidence)
    $rgLower = $resourceGroup.ToLower()
    
    foreach ($keyword in $sandboxKeywords) {
        if ($rgLower -match $keyword) {
            return @{
                Tier = "Sandbox"
                Confidence = "Low"
                Source = "Resource Group: $resourceGroup"
            }
        }
    }
    
    foreach ($keyword in $devTestKeywords) {
        if ($rgLower -match $keyword) {
            return @{
                Tier = "Dev/Test"
                Confidence = "Low"
                Source = "Resource Group: $resourceGroup"
            }
        }
    }
    
    foreach ($keyword in $prodKeywords) {
        if ($rgLower -match $keyword) {
            return @{
                Tier = "Production"
                Confidence = "Low"
                Source = "Resource Group: $resourceGroup"
            }
        }
    }
    
    # Priority 4: Check resource name (lowest confidence)
    $resNameLower = $resourceName.ToLower()
    
    foreach ($keyword in $devTestKeywords) {
        if ($resNameLower -match $keyword) {
            return @{
                Tier = "Dev/Test"
                Confidence = "Low"
                Source = "Resource Name: $resourceName"
            }
        }
    }
    
    foreach ($keyword in $prodKeywords) {
        if ($resNameLower -match $keyword) {
            return @{
                Tier = "Production"
                Confidence = "Low"
                Source = "Resource Name: $resourceName"
            }
        }
    }
    
    # Default: Assume production (safe default requiring customer review)
    return @{
        Tier = "Production (Assumed)"
        Confidence = "Low"
        Source = "Default (No classification keywords found)"
    }
}

